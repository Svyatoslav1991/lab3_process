#pragma once

#include <QSharedMemory>
#include <QString>
#include <QDataStream>

/**
 * @file sharedmemorychannel.h
 * @brief Объявление класса-обёртки для обмена строками через QSharedMemory.
 *
 * @details
 * Класс SharedMemoryChannel инкапсулирует работу с сегментом разделяемой памяти
 * и реализует простой двоичный протокол хранения данных:
 * - в начале сегмента располагается служебный заголовок Header;
 * - после заголовка располагается полезная нагрузка в виде сериализованной строки.
 *
 * Строка сериализуется через QDataStream в QByteArray, после чего массив байт
 * копируется в сегмент разделяемой памяти. Для чтения выполняется обратная операция:
 * чтение заголовка, проверка формата и десериализация строки из полезной нагрузки.
 *
 * Такой подход позволяет:
 * - явно контролировать формат данных в памяти;
 * - проверять корректность содержимого по сигнатуре и версии протокола;
 * - безопасно читать и записывать данные из разных процессов при помощи блокировки.
 */

/**
 * @class SharedMemoryChannel
 * @brief Канал обмена строковыми данными через разделяемую память.
 *
 * @details
 * Класс предоставляет высокоуровневый интерфейс поверх QSharedMemory для типового
 * сценария лабораторной работы: запись и чтение строки между процессами.
 *
 * Основные возможности:
 * - создание сегмента разделяемой памяти;
 * - подключение к уже существующему сегменту;
 * - отключение от сегмента;
 * - запись строки в сегмент памяти;
 * - чтение строки из сегмента памяти;
 * - выдача диагностических сообщений об ошибках.
 *
 * Формат данных в памяти:
 * - заголовок Header с сигнатурой, версией протокола и размером полезной нагрузки;
 * - полезная нагрузка в виде QByteArray, полученного сериализацией QString.
 *
 * Для синхронизации доступа к памяти используется вложенный RAII-класс LockGuard,
 * автоматически вызывающий lock()/unlock() у QSharedMemory.
 *
 * @note
 * Класс ориентирован именно на обмен строкой. При необходимости его можно
 * расширить для передачи более сложных структур, сохранив тот же принцип:
 * сериализация в буфер и запись буфера в разделяемую память.
 *
 * @note
 * Экземпляр класса не является QObject и не использует сигналы/слоты.
 */
class SharedMemoryChannel final
{
public:
    /**
     * @brief Размер сегмента разделяемой памяти по умолчанию.
     *
     * @details
     * Значение используется, если пользователь явно не задал размер сегмента
     * в конструкторе. Размер подобран с запасом для демонстрационного обмена
     * строковыми данными.
     */
    static constexpr int kDefaultSegmentSize = 4096;

    /**
     * @brief Возвращает ключ сегмента разделяемой памяти по умолчанию.
     * @return Строковый ключ, используемый для идентификации сегмента в ОС.
     *
     * @details
     * Родительский и дочерний процессы должны использовать один и тот же ключ,
     * иначе они будут работать с разными сегментами памяти.
     */
    static QString defaultKey();

    /**
     * @brief Создаёт объект канала работы с разделяемой памятью.
     * @param key Ключ сегмента разделяемой памяти.
     * @param segmentSize Желаемый размер сегмента в байтах.
     *
     * @details
     * Если переданный размер слишком мал, он автоматически увеличивается до
     * минимально допустимого: размер заголовка Header + минимум 1 байт данных.
     *
     * @note
     * Конструктор только подготавливает объект QSharedMemory, но не создаёт
     * сегмент и не подключается к нему автоматически.
     */
    explicit SharedMemoryChannel(const QString& key = defaultKey(),
                                 int segmentSize = kDefaultSegmentSize);

    /**
     * @brief Создаёт сегмент разделяемой памяти или подключается к уже существующему.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return true при успехе, иначе false.
     *
     * @details
     * Поведение метода:
     * - если объект уже подключён к сегменту, метод сразу возвращает true;
     * - если сегмент удалось создать, память инициализируется нулями и в начало
     *   записывается служебный заголовок с сигнатурой и версией протокола;
     * - если сегмент уже существует, выполняется попытка attach();
     * - при ошибке возвращается false и, при наличии указателя, заполняется
     *   текст ошибки.
     */
    bool create(QString* errorMessage = nullptr);

    /**
     * @brief Подключается к уже существующему сегменту разделяемой памяти.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return true при успехе, иначе false.
     *
     * @details
     * Метод предназначен прежде всего для дочернего процесса, который должен
     * подключиться к уже созданному сегменту.
     */
    bool attach(QString* errorMessage = nullptr);

    /**
     * @brief Отключается от сегмента разделяемой памяти.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return true при успехе, иначе false.
     *
     * @details
     * Если объект не подключён к сегменту, метод считает это корректным
     * состоянием и возвращает true.
     */
    bool detach(QString* errorMessage = nullptr);

    /**
     * @brief Проверяет, подключён ли объект к сегменту памяти.
     * @return true, если сегмент присоединён; иначе false.
     */
    bool isAttached() const;

    /**
     * @brief Возвращает текст последней ошибки QSharedMemory.
     * @return Строка с описанием ошибки низкоуровневого объекта памяти.
     */
    QString errorString() const;

    /**
     * @brief Возвращает фактически используемый размер сегмента.
     * @return Размер сегмента в байтах.
     *
     * @details
     * Возвращаемое значение может быть больше исходного аргумента конструктора,
     * если тот был меньше минимально допустимого размера.
     */
    int segmentSize() const;

    /**
     * @brief Сериализует и записывает строку в разделяемую память.
     * @param value Строка для записи.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return true при успехе, иначе false.
     *
     * @details
     * Последовательность работы:
     * - проверяется наличие подключения к сегменту памяти;
     * - строка сериализуется в QByteArray;
     * - вычисляется полный объём данных: заголовок + полезная нагрузка;
     * - выполняется блокировка сегмента памяти;
     * - память очищается;
     * - записывается заголовок Header;
     * - затем записывается сериализованная строка.
     *
     * @note
     * Если сериализованные данные не помещаются в сегмент памяти,
     * метод завершится с ошибкой.
     */
    bool writeString(const QString& value, QString* errorMessage = nullptr);

    /**
     * @brief Считывает и десериализует строку из разделяемой памяти.
     * @param value Указатель для возврата прочитанной строки.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return true при успехе, иначе false.
     *
     * @details
     * Последовательность работы:
     * - проверяется наличие подключения к сегменту памяти;
     * - выполняется блокировка сегмента;
     * - считывается заголовок Header;
     * - проверяются сигнатура, версия и размер полезной нагрузки;
     * - из памяти извлекается полезная нагрузка;
     * - выполняется десериализация строки из QByteArray.
     *
     * @note
     * Если сегмент ещё не содержит осмысленных данных и заполнен нулями,
     * метод считает это допустимой ситуацией и возвращает пустую строку.
     */
    bool readString(QString* value, QString* errorMessage = nullptr);

private:
    /**
     * @struct Header
     * @brief Служебный заголовок данных в сегменте разделяемой памяти.
     *
     * @details
     * Заголовок располагается в самом начале сегмента и содержит:
     * - magic — сигнатуру формата;
     * - version — версию протокола обмена;
     * - payloadSize — размер полезной нагрузки в байтах.
     *
     * Наличие заголовка позволяет проверить, что в памяти действительно лежат
     * данные ожидаемого формата, а также понять, сколько байт необходимо
     * прочитать после заголовка.
     */
    struct Header
    {
        /// Сигнатура формата данных.
        quint32 magic = 0;

        /// Версия протокола сериализации.
        quint32 version = 0;

        /// Размер сериализованной полезной нагрузки в байтах.
        quint32 payloadSize = 0;
    };

    /**
     * @class LockGuard
     * @brief RAII-обёртка для блокировки сегмента разделяемой памяти.
     *
     * @details
     * При создании объект пытается вызвать QSharedMemory::lock(),
     * а при уничтожении автоматически вызывает unlock(), если блокировка
     * была успешно получена.
     *
     * Это гарантирует корректное освобождение блокировки даже при раннем выходе
     * из функции, что повышает надёжность кода чтения и записи.
     */
    class LockGuard final
    {
    public:
        /**
         * @brief Пытается заблокировать сегмент разделяемой памяти.
         * @param memory Объект QSharedMemory, который необходимо заблокировать.
         */
        explicit LockGuard(QSharedMemory& memory);

        /**
         * @brief Освобождает блокировку, если она была успешно получена.
         */
        ~LockGuard();

        /**
         * @brief Проверяет, была ли блокировка успешно установлена.
         * @return true, если lock() выполнился успешно; иначе false.
         */
        bool locked() const;

    private:
        /// Ссылка на объект разделяемой памяти.
        QSharedMemory& memory_;

        /// Признак успешного захвата блокировки.
        bool locked_ = false;
    };

private:
    /**
     * @brief Сигнатура формата данных в заголовке.
     *
     * @details
     * Значение записывается в Header::magic и используется при чтении для
     * проверки того, что содержимое памяти соответствует ожидаемому протоколу.
     */
    static constexpr quint32 kMagic = 0x4C334D51; // "L3MQ"

    /**
     * @brief Поддерживаемая версия протокола хранения данных.
     *
     * @details
     * Значение записывается в Header::version. При чтении несовпадение версии
     * считается ошибкой несовместимости формата.
     */
    static constexpr quint32 kProtocolVersion = 1;

    /**
     * @brief Версия QDataStream, используемая при сериализации строки.
     *
     * @details
     * Одинаковая версия потока должна использоваться как при записи,
     * так и при чтении, чтобы двоичный формат данных интерпретировался
     * согласованно.
     */
    static constexpr int kStreamVersion = QDataStream::Qt_5_12;

    /**
     * @brief Сериализует строку в массив байт.
     * @param value Исходная строка.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return QByteArray с сериализованными данными либо пустой массив при ошибке.
     *
     * @details
     * Метод создаёт QBuffer поверх QByteArray и записывает строку в буфер
     * при помощи QDataStream.
     */
    QByteArray serializeString(const QString& value, QString* errorMessage) const;

    /**
     * @brief Десериализует строку из массива байт.
     * @param payload Полезная нагрузка, считанная из памяти.
     * @param value Указатель для возврата результата.
     * @param errorMessage Необязательный указатель для возврата текста ошибки.
     * @return true при успешной десериализации, иначе false.
     *
     * @details
     * Метод создаёт QBuffer для чтения из QByteArray и извлекает QString
     * через QDataStream.
     */
    bool deserializeString(const QByteArray& payload, QString* value, QString* errorMessage) const;

    /**
     * @brief Записывает текст ошибки в выходной параметр.
     * @param errorMessage Указатель на строку для сообщения об ошибке.
     * @param text Текст ошибки.
     * @return Всегда возвращает false.
     *
     * @details
     * Вспомогательный метод используется для унификации обработки ошибок
     * и сокращения дублирования кода.
     */
    bool setError(QString* errorMessage, const QString& text) const;

private:
    /// Низкоуровневый объект Qt для работы с разделяемой памятью.
    QSharedMemory memory_;

    /// Размер сегмента разделяемой памяти, который ожидает использовать объект.
    int segmentSize_ = kDefaultSegmentSize;
};
